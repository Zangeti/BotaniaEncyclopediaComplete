class PlantIdentifier {
  final String scientificName;
  final String commonName;
  final String sowingInstructions;
  final String spaceInstructions;
  final String harvestInstructions;
  final String compatiblePlants;
  final String avoidInstructions;
  final String culinaryHints;
  final String culinaryPreservation;
  final String url;

  PlantIdentifier(this.scientificName, this.commonName,
    this.sowingInstructions, this.spaceInstructions, this.harvestInstructions,
    this.compatiblePlants, this.avoidInstructions, this.culinaryHints, 
    this.culinaryPreservation, this.url);
}

